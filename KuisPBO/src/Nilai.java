public class Nilai {

    public float RataRata (float nilai1, float nilai2, float nilai3, float nilai4) {
        return (nilai1 + nilai2 + nilai3 + nilai4)/4;
    }

    public float getRataRata(float nilai1, float nilai2, float nilai3, float nilai4) {
        return RataRata(nilai1, nilai2, nilai3, nilai4);
    }

}
